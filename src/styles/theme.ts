// Export some useful theme helpers
export const getColor = (colorName: string, shade: number = 500): string | undefined => {
  const colorPath = colorName.split('.');
  let color: Record<string, unknown> | unknown = theme.colors;
  
  // Navigate through nested color properties
  for (const path of colorPath) {
    if (typeof color === 'object' && color !== null && path in color) {
      color = (color as Record<string, unknown>)[path];
    } else {
      return undefined;
    }
  }
  
  return typeof color === 'object' && color !== null && shade in color 
    ? (color as Record<number, string>)[shade] 
    : typeof color === 'string' ? color : undefined;
};

